<template>
  <div class="prompt-input">
    <prompt />
    <form v-on:submit.prevent="onSubmit">
        <input type="text"
               v-focus
               v-model.trim="cmd" />
    </form>
  </div>
</template>

<script>
import Prompt from './Prompt.vue'

export default {
  name: 'prompt-input',
  components: {
    Prompt
  },
  data () {
    return {
      cmd: ''
    }
  },
  methods: {
    onSubmit () {
      this.$emit('exec', { cmd: this.cmd })
      this.cmd = ''
    }
  }
}
</script>

